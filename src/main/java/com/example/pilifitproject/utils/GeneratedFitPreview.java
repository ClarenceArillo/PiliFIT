package com.example.pilifitproject.utils;

import com.example.pilifitproject.model.ClothingItem;

public class GeneratedFitPreview {
        private ClothingItem top;
        private ClothingItem bottom;
        private ClothingItem shoes;

        // Constructor, getters
        public GeneratedFitPreview(ClothingItem top, ClothingItem bottom, ClothingItem shoes) {
            this.top = top;
            this.bottom = bottom;
            this.shoes = shoes;
        }

        public ClothingItem getTop() { return top; }
        public ClothingItem getBottom() { return bottom; }
        public ClothingItem getShoes() { return shoes; }
    }


